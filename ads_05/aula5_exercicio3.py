print("Programa que calcula o consumo médio de um automóvel:")
distancia_percorrida = int(input("Informe a distância total percorrida pelo o carro:"))
litrogasolina_consumido = int(input("Informe a quantidade de combustível consumido em litros: "))
consumo_medio = distancia_percorrida/litrogasolina_consumido
print(f"O consumo médio do automóvel é: {consumo_medio}")

